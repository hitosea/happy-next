import { build } from 'esbuild';
import { fileURLToPath } from 'url';
import { readFile, writeFile } from 'fs/promises';
import path from 'path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '../..');

const bundlePath = path.join(root, 'sources/components/codemirror-bundle.js');
const stringPath = path.join(root, 'sources/components/codemirror-bundle-string.ts');

await build({
  entryPoints: [path.join(root, 'sources/components/codemirror-bundle-entry.ts')],
  bundle: true,
  format: 'iife',
  minify: true,
  outfile: bundlePath,
  target: ['es2020'],
  platform: 'browser',
});

console.log('codemirror-bundle.js built successfully');

// Generate TypeScript string wrapper for Metro compatibility
const bundleSource = await readFile(bundlePath, 'utf-8');
const escaped = bundleSource.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
const wrapper = `// Auto-generated by build-codemirror.mjs — do not edit\nexport default \`${escaped}\`;\n`;
await writeFile(stringPath, wrapper, 'utf-8');

console.log('codemirror-bundle-string.ts generated successfully');
